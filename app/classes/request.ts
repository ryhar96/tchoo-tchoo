export class Request {

    public srcX: number;
    public srcY: number;
    public destX: number;
    public destY: number;

    constructor(srcX: number, srcY: number, destX: number, destY: number) {
        this.srcX = srcX;
        this.srcY = srcY;
        this.destX = destX;
        this.destY = destY;
    }

}